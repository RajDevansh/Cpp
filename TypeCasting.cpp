#include<iostream>
using namespace std;
int main() {
    float a=23.34;
    int b=34;
    cout<<"Converting float to int: "<<(int)a<<endl; 
    cout<<"Converting float to int: "<<int(a)<<endl;
    cout<<"Converting int to float: "<<(float)b<<endl;
    cout<<"Converting int to float: "<<float(b)<<endl;
    return 0;
}
